//需求：打印常量。
//声明一个类，名叫Constant02
class Constant02{
	//定义主方法，程序的入口。
	public static void main(String[] args){
		//输出整数
		System.out.println(905);
		//输出小数
		System.out.println(36.258);
		//输出boolean类型的值。
		System.out.println(true);
		System.out.println(false);
		//输出字符串，若想在字符串里输出双引号，要在前面加转义符（饭斜杠）
		System.out.println("中国有句古话叫\"闷声发大财\",识得唔识得？");
	}
}
